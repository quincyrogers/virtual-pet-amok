package roboticbuddies;

public class RoboticCat {

	private String name;
	private int health;
	private int happiness;
	private int oillevel;

	public RoboticCat(String name, int health, int happiness, int oillevel) {
		
	}

	public String getName() {
		
		return name;
	}

	public int getHealth() {
		
		return health;
	}

	public int getHappiness() {
		
		return happiness;
	}

	public int getOilLevel() {
		
		return oillevel;
	}

}
